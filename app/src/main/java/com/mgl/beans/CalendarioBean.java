package com.mgl.beans;

public class CalendarioBean {
	
	String IdCalendario;
	String NomMesCalendario;
	String NumMesCalendario;
	String NumDiaCalendario;
	String FondoCalendario;
	String EsColor;
	
	public String getIdCalendario() {
		return IdCalendario;
	}
	public void setIdCalendario(String idCalendario) {
		IdCalendario = idCalendario;
	}
	public String getNomMesCalendario() {
		return NomMesCalendario;
	}
	public void setNomMesCalendario(String nomMesCalendario) {
		NomMesCalendario = nomMesCalendario;
	}
	public String getNumMesCalendario() {
		return NumMesCalendario;
	}
	public void setNumMesCalendario(String numMesCalendario) {
		NumMesCalendario = numMesCalendario;
	}
	public String getNumDiaCalendario() {
		return NumDiaCalendario;
	}
	public void setNumDiaCalendario(String numDiaCalendario) {
		NumDiaCalendario = numDiaCalendario;
	}
	public String getFondoCalendario() {
		return FondoCalendario;
	}
	public void setFondoCalendario(String fondoCalendario) {
		FondoCalendario = fondoCalendario;
	}
	public String getEsColor() {
		return EsColor;
	}
	public void setEsColor(String esColor) {
		EsColor = esColor;
	}
	
}
